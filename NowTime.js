function Hours()
{
    var nowDate = new Date();
    var nowHours = nowDate.getHours();
    return nowHours;
}

function Minutes()
{
    var nowDate = new Date();
    var nowMinutes = nowDate.getMinutes();
    return nowMinutes;
}

function Seconds()
{
    var nowDate = new Date();
    var nowSeconds = nowDate.getSeconds();
    return nowSeconds;
}
